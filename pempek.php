        <ul>
            <li>Resep : <br>
                1 kg daging ikan tenggiri yang telah digiling halus <br>
                1 kg tepung sagu <br>
                250 ml air es <br>
                penyedap rasa secukupnya <br>
                garam secukupnya <br><br>

                Bumbu cuko : <br>
                750 ml air <br>
                500 gram gula batok Palembang <br>
                125 gram cabai rawit hijau <br>
                75 gram bawang putih <br>
                200 gram gula pasir <br>
                4 sdm cuka putih <br>
                20 gram garam <br><br>

            </li>
            <li> Cara membuat : <br>
                1. Masukkan ikan, garam, penyedap rasa dan air es, lalu campurkan hingga merata <br>
                2. Tambahkan tepung sedikit demi sedikit, aduk hingga adonan menjadi kalis <br>
                3. Bentuk adonan sesuai seleramu.Rebus adonan pempek yang telah dibentuk ke air mendidih <br>
                4. Jika adonan telah mengapung, itu artinya pempek sudah matang. Tiriskan <br>
                5. Kamu bisa langsung menyajikannya atau bisa digoreng dulu <br>
                6. Jangan lupa gunakan cuko biar lebih mantap <br><br>

                Cara membuat bumbu cuko : <br>
                1. Rebus air bersama gula merah hingga larut sepenuhnya. Setelah itu angkat dan saring airnya <br>
                2. Rebus kembali larutan gula merah bersama bumbu halus, garam, gula pasir, ebi yang sudah dihaluskan
                dan asam jawa <br>
                3. Masak dengan api kecil sampai airnya sedikit menyusut <br>
                4. Jika cuko sudah mencapai kekentalan yang diinginkan, segera angkat dari kompor <br>
                5. Saring cuko, kemudian tempatkan di wadah. Jika sudah dingin, tutup rapat dan masukkan ke dalam lemari
                es <br><br>
            </li>
        </ul>

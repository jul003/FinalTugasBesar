                    3. Rebus dengan api kecil hingga daging lunak dan kuahnya berkurang <br>
                    4. Haluskan semua bahan bumbu lalu masukkan ke dalam rebusan daging. Masak hingga mendidih dan bumbu
                    meresap <br>
                    5. Masukkan irisan lobak, didihkan hingga lunak. Angkat <br>
                    6. Sajikan soto bandung dengan taburan kedelai goreng, daun bawang dan bawang merah goreng serta
